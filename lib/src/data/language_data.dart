  /// Get all languages (default and extended)
  static List<Language> get allLanguages => [...defaultLanguages, ...extendedLanguages.where(
          (extended) => !defaultLanguages.any((def) => def.code == extended.code)
  )];

  /// Get a language by its code
  static Language getLanguageByCode(LanguageCode code) {
    return allLanguages.firstWhere(
          (language) => language.code == code,
      orElse: () => allLanguages.firstWhere((language) => language.code == LanguageCode.en),
    );
  }

  /// Get a language by its ISO code string
  static Language getLanguageByIsoCode(String isoCode) {
    return allLanguages.firstWhere(
          (language) => language.isoCode == isoCode,
      orElse: () => allLanguages.firstWhere((language) => language.code == LanguageCode.en),
    );
  }
